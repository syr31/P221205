package WrapperExample_02;

public class Main {
	public static void main(String[] args) {
		Integer obj = 100;
		System.out.println("Value : " + obj.intValue());

		int value = obj;
		System.out.println("value : " + value);

		int result = obj + 100;
		System.out.println("result : " + result);

	}

}
